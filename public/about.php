<br/>
ChipSelect wants to make embedded development easier.<br/>
We want to provide all information that can help with making the decision which chips to use. This includes how to
program them, how to test software for them, how to switch between them and how to debug them.<br/>
<br/>

Existing data about microcontrollers
<ul>
<li>- is scattered over web sites from different vendors</li>
<li>- differs in form and quality from vendor to vendor</li>
<li>- is hard to find</li>
<li>- has errors</li>
</ul>
<br/>
ChipSelect collects all available data on programmable chips, such as microcontrollers and FPGAs.
This includes characteristics as well as peripheral information.<br/>
<br/>
ChipSelect wants to become the central database
aggregating all information on programmable chips.<br/>
<br/>
We hope that this information will help:
<ul>
<li>- embedded system designers</li>
<li>- embedded software developers</li>
<li>- PCB designers</li>
<li>- IDE and tool developers</li>
<li>- everybody working with programmable chips</li>
</ul>
<br/>

ChipSelect will try to make this data available in all forms
that benefit the users.<br/>

ChipSelect is community-based. It relies on its community to
correct and complete the information. We can therefore not guarantee
that all information will always be correct or even complete.<br/>
<br/>
If you want to help, have information that is not yet included or need information in a different format, please contact us!<br/>
<br/>
ChipSelect is a non-profit organization.<br/>
The information collected by ChipSelect is available
to the everybody as freely as possible.<br/>
This also includes commercial proprietary software.
If the information is used in software we suggest to let the user know the source information.
This way the user will know to address chipselect if the information is not up to date.<br/>
<br/>
If you think that some of the informations presented on chipselct
should not be publicly available because it is protected by intellectual property- or copy-rights, then please contact us so that we can correct the situation.
